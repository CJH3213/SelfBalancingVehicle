/* Includes ------------------------------------------------------------------*/
#include "CD4051.h"

void CD4051_OutPut(uint8_t port, uint8_t level)
{
	(port&0x04)? (CD4051_C_Set()):(CD4051_C_Clr());
	(port&0x02)? (CD4051_B_Set()):(CD4051_B_Clr());
	(port&0x01)? (CD4051_A_Set()):(CD4051_A_Clr());
	level? (CD4051_COM_Set()):(CD4051_COM_Clr());
}
